#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	long			n;
	long			minus;

	n = 0;
	i = 0;
	minus = 1;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' || \
		str[i] == ' ' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus = -1;
		i++;
	}
	if (!(str[i] >= '0' && str[i] <= '9'))
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = 10 * n + (str[i] - '0');
		i++;
	}
	return ((int)(n * minus));
}
