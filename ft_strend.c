#include "libft.h"

int	ft_strend(char *s1, char *s2)
{
	s1 += ft_strlen(s1) - ft_strlen(s2);
	while (*s2)
	{
		if (*s2++ != *s1++)
			return (0);
	}
	return (1);
}
