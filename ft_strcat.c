char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int len1;
	int len2;

	len1 = 0;
	while (s1[len1] != '\0')
	{
		len1++;
	}
	len2 = 0;
	while (s2[len2] != '\0')
	{
		len2++;
	}
	i = 0;
	while (i < len2)
	{
		s1[i + len1] = s2[i];
		i++;
	}
	s1[i + len1] = '\0';
	return (s1);
}
